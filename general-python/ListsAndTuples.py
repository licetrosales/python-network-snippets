# List and tuples
list1 = ["January", "Four", 2025, 11.56, "Heaven"]
tuple1 = ("a", "Sydney", "1900", 3.1416, 0.01, "Pathon")
tuple2 = "x", "empire", "lego", 1, 2.0, 7
print (list1)
print (tuple1)
print (tuple2)

# Accessing values in lists and tuples
print (list1[0:2])
print (list1 [2:])
print (tuple1[1:5])
print (tuple2[0:-1])
